export function getLandmarkUrduSection(
  slugOrName: string | undefined,
  section: UrduSection = "summary",
): string | null {
  if (!slugOrName) return null;
  const key = normalizeLandmarkKey(slugOrName);
  const bundle = LANDMARK_URDU[key];
  if (!bundle) {
    const partialKey = Object.keys(LANDMARK_URDU).find(
      (k) => key.includes(k) || k.includes(key),
    );
    if (partialKey) {
      return LANDMARK_URDU[partialKey][section] ?? LANDMARK_URDU[partialKey].summary ?? null;
    }
    return null;
  }
  return bundle[section] ?? bundle.summary ?? null;
}

export function getGenericUrduSection(section: UrduSection): string {
  return GENERIC_URDU[section];
}
